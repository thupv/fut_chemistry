};

extension LatinExtended on String {
  removeLatinExtended() {
    var string = this;
    // Initialize an empty StringBuffer
    StringBuffer sb = StringBuffer();

    // Iterate through the characters in the input string
    for (int i = 0; i < string.length; i++) {
      // If the character is a Latin extended character, convert it to a Latin character
      // and append it to the StringBuffer
      if (latinExtendedToLatin.containsKey(this[i])) {
        sb.write(latinExtendedToLatin[this[i]]);
      }
      // Otherwise, just append the character to the StringBuffer
      else {
        sb.write(this[i]);
      }
    }

    // Return the resulting string
    return sb.toString();
  }
}